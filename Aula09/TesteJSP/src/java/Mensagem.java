public class Mensagem {
    private String text ;
    private String user ;
    private Date time;

    public Mensagem(String text, String user) {
        this.text = text;
        this.user = user;
        this.time = new Date();
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }  
            
}
